public class Villas {
	private int numVilla;
	private String nameVilla;

	public Villas(String name) {
		this.nameVilla = name;
	}

	public int getNumVilla() {
		return numVilla;
	}

	public void setNumVilla(int numVilla) {
		this.numVilla = numVilla;
	}

	public String getNameVilla() {
		return nameVilla;
	}

	public void setNameVilla(String nameVilla) {
		this.nameVilla = nameVilla;
	}

}
